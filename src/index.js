const { ApolloServer } = require('apollo-server');
const typeDefs = require('./schema');
const resolvers = require('./resolvers');
const TrackAPI = require('./datasources/track-api');

async function startApolloServer(typeDefs, resolvers) {
  const server = new ApolloServer({
    typeDefs,
    resolvers,
    dataSources: () => {
      return {
        trackAPI: new TrackAPI(),
      };
    },
  });
  //para producciÃ³n necesitamos decirle un puerto el cual lo seteamos en .env, como hicimos deploy en heroku el mismo configura el puerto para usar, simplemente con process.env.PORT le decimos que lo que sea que este en esa variable lo agarre
  const { url, port } = await server.listen({port: process.env.PORT || 4000});
  console.log(`
      ðŸš€  Server is running
      ðŸ”‰  Listening on port ${port}
      ðŸ“­  Query at ${url}
    `);
}

startApolloServer(typeDefs, resolvers);
